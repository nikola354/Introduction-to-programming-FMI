#include <iostream>

using namespace std;

int main()
{
	char ch;

	cin >> ch;

	switch (ch)
	{
	case 'I':
		cout << (int)ch << endl;
		break;
	case 'V':
		cout << (int)ch << endl;
		break;
	case 'X':
		cout << (int)ch << endl;
		break;
	case 'L':
		cout << (int)ch << endl;
		break;
	case 'C':
		cout << (int)ch << endl;
		break;
	case 'D':
		cout << (int)ch << endl;
		break;
	case 'M':
		cout << (int)ch << endl;
		break;
	default: 
		cout << "-1" << endl;
		break;
	}

	return 0;
}
